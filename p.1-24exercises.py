# 24 - Make_a_calculator.

input_1 = int(input("Please enter the first number"))
input_2 = int(input("Please enter the second number"))

addition = int(input_1 + input_2)

subtraction = int(input_1 - input_2)

multiplication = int(input_1 * input_2)

division = float(input_1 // input_2)

exponential = int(input_1 ** input_2)

print(f"{input_1} + {input_2} is equals to {addition}")
print(f"{input_1} - {input_2} is equals to {subtraction}")
print(f"{input_1} * {input_2} is equals to {multiplication}")
print(f"{input_1} + {input_2} is equals to {division}")
print(f"{input_1} ** {input_2} is equals to {exponential}")


